  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
	
	HAL_ADC_Start_DMA(&hadc1,(uint32_t *)ADC_BUFF,fftSize); 	// start next 1024 sample
	
	TIM4->CCR1=2048;
	TIM4->CCR2=2048;
	TIM4->CCR3=2048;
