static void rfft_run(void)
{
	static arm_rfft_fast_instance_f32 rfft;		/** Instance structure for RFFT. */
	
	arm_rfft_fast_init_f32(&rfft, fftSize);
	memset(FFTResult, 0, sizeof(FFTResult));

	arm_rfft_fast_f32(&rfft, FFTInput, FFTResult, ifftFlag);
}

static void cfft_run(void)
{
  arm_cfft_f32(&arm_cfft_sR_f32_len1024, FFTInput, ifftFlag, doBitReverse);		//* Process the data through the CFFT/CIFFT module 
  arm_cmplx_mag_f32(FFTInput, FFTResult, fftSize);														//* Process the data through the Complex Magnitude Module for calculating the magnitude at each bin
}


